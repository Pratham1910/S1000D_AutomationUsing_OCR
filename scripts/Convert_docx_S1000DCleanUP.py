        self.step2_status = ttk.Label(
            status_frame,
            text="Pending",
            font=self.status_font,
            foreground=self.status_pending_fg,
            background=self.frame_bg,
        )
        self.step2_status.grid(row=1, column=2, sticky="e", padx=5)

