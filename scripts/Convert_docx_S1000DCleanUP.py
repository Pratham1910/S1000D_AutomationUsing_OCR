def cleanup_adoc_string(adoc_content_string, log_func):
    """
    Applies general cleanup and fixes to the generated AsciiDoc content (in-memory string),
    reversing the over-aggressive list insertion and fixing step attributes.
    """
    log_func(f"    Applying in-memory cleanup logic...")
    try:
        content = adoc_content_string

        # --- Structural Cleanup ---

        # 1. FIX: Remove isolated, empty AsciiDoc section headers (e.g., '== \n *')
        cleaned_content = re.sub(r"^\s*=+?\s*$", r"", content, flags=re.MULTILINE)

        # 2. FIX: Remove unwanted soft line breaks ('+') generated by Pandoc
        cleaned_content = re.sub(r",\s*\n\s*\+\s*\n", r",\n", cleaned_content)
        cleaned_content = re.sub(r"([^\n,])\n\s*\+\s*\n", r"\1\n", cleaned_content)
        cleaned_content = re.sub(
            r"(.+?)\s*\+\s*$", r"\1", cleaned_content, flags=re.MULTILINE
        )

        # --- FIX: Reversing over-aggressive list insertion (dots everywhere) ---

        # 3. CRITICAL FIX: Remove misplaced dots and unwanted attributes:
        # a) Dots before section headers (e.g., `.==`, `.===`)
        cleaned_content = re.sub(
            r"^\s*\.(\=+)\s+", r"\1 ", cleaned_content, flags=re.MULTILINE
        )

        # b) Dots before list attributes (e.g., `.[arabic]`, `.[start=2]`)
        cleaned_content = re.sub(
            r"^\s*\.(\[.+?\])", r"\1", cleaned_content, flags=re.MULTILINE
        )

        # d) Fix table header marker issue
        cleaned_content = re.sub(
            r"^\s*\.\|===", r"|===", cleaned_content, flags=re.MULTILINE
        )

        # e) NEW FIX: Remove [arabic] or [arabic, start=X] attributes (as requested)
        cleaned_content = re.sub(
            r"\[arabic(\s*,\s*start=\d+)?\]\s*",
            r"",
            cleaned_content,
            flags=re.MULTILINE,
        )

        # This secondary fix catches any standalone dot on a line that should be part of the following list content
        cleaned_content = re.sub(
            r"^\s*\.\s*$\n([A-Z0-9\[].*)", r". \1", cleaned_content, flags=re.MULTILINE
        )

        # The sub-steps also need to be cleaned of extra dots/spaces if they lose their parent attribute
        cleaned_content = re.sub(
            r"^\s*\.\s+Sub-step", r".. Sub-step", cleaned_content, flags=re.MULTILINE
        )

        # --- General Cleanup (Final pass for previously working fixes) ---
        cleaned_content = re.sub(
            r"\{\s*empty\s*\}", r"", cleaned_content, flags=re.MULTILINE
        )
        cleaned_content = re.sub(
            r"^\s*\{plus\}\s*$", r"+", cleaned_content, flags=re.MULTILINE
        )
        cleaned_content = re.sub(
            r"^\s*____\s*$", r"", cleaned_content, flags=re.MULTILINE
        )
        cleaned_content = re.sub(r"(--\n)\s+", r"\1", cleaned_content)
        cleaned_content = re.sub(r"\s+(\n--)", r"\1", cleaned_content)

        # --- CROSS-REFERENCE FIXES (<<ref>>) ---
        cleaned_content = re.sub(r"<<\s*(.+?)\s*>>", r"<<\1>>", cleaned_content)
        cleaned_content = re.sub(
            r"<<(.*)([\.\,\?\!\:\;])>>", r"<<\1>>\2", cleaned_content
        )

        # FIX 4: Join multi-line attribute blocks `[...]` into a single line
        lines = cleaned_content.splitlines()
        rebuilt_lines = []
        in_attribute_block = False
        for line in lines:
            stripped_line = line.strip()
            if stripped_line.startswith("[") and not stripped_line.endswith("]"):
                in_attribute_block = True
                rebuilt_lines.append(line.rstrip())
            elif in_attribute_block:
                rebuilt_lines[-1] += " " + stripped_line.replace("`", "")
                if stripped_line.endswith("]"):
                    in_attribute_block = False
            else:
                rebuilt_lines.append(line)
        cleaned_content = "\n".join(rebuilt_lines)

        # FIX 5: Normalize spacing around thematic breaks (---)
        cleaned_content = re.sub(
            r"\s*^\s*-{3,}\s*$\s*", r"\n\n---\n\n", cleaned_content, flags=re.MULTILINE
        )

        # --- ANCHOR ID FIXES ([[id]]) ---
        cleaned_content = re.sub(r"([^\n])(\[\[.+?\]\])", r"\1\n\2", cleaned_content)

        # FIX 7: Ensure a blank line between a closing delimiter and a block anchor
        cleaned_content = re.sub(
            r"(--\s*\n)(\[\[.+?\]\])", r"\1\n\2", cleaned_content, flags=re.MULTILINE
        )

        cleaned_content = re.sub(r"(\[\[.+?\]\])([^\n])", r"\1\n\2", cleaned_content)

        # --- CRITICAL FIX: Ensure exactly one space after list markers (. or ..) ---
        # This is a defensive, late-stage fix to ensure list items always have correct spacing
        # Example: ".Item" becomes ". Item", "..SubItem" becomes ".. SubItem"
        cleaned_content = re.sub(
            r"^((\.{1,2}))\s*(?=\S)",  # Capture one or two dots, then zero or more spaces, followed by non-whitespace
            r"\1 ",  # Replace with captured dots and exactly one space
            cleaned_content,
            flags=re.MULTILINE,
        )

        # --- Final Cleanup ---
        cleaned_content = re.sub(r"\n\s*\n", r"\n\n", cleaned_content)
        return cleaned_content

    except Exception as e:
        log_func(
            f"    ERROR: Failed to apply in-memory cleanup logic.\n    Details: {e}"
        )
        return adoc_content_string  # Return original content if cleanup fails


def process_dmc_filename(filename, log_func):
    """Converts a standardized DMC filename format to a new, structured format."""
    if not filename.startswith("DMC-"):
        return filename

    base_code = filename[4:]
    parts = base_code.split("-")

    if len(parts) == 8:
        p1, p2, p3, p4, p5, p6, p7, p8 = parts
        subSystemCode = p4[0] if len(p4) > 0 else ""
        subSubSystemCode = p4[1] if len(p4) > 1 else ""
        disassyCode = p6[:-1] if len(p6) > 0 else ""
        disassyCodeVariant = p6[-1] if len(p6) > 0 else ""
        infoCode_raw = p7[:-1] if len(p7) > 0 else ""
        infoCodeVariant = p7[-1] if len(p7) > 0 else ""
        infoCode = infoCode_raw.zfill(3)
        final_parts = [
            p1,
            p2,
            p3,
            subSystemCode,
            subSubSystemCode,
            p5,
            disassyCode,
            disassyCodeVariant,
            infoCode,
            infoCodeVariant,
            p8,
        ]
        return "DMC-" + "-".join(final_parts)
    return filename


# Helper function to ensure the Lua filter file exists
def ensure_lua_filter_exists(log_func):
    """Writes the Lua filter content to the expected file path."""
    global LUA_FILTER_PATH, LUA_FILTER_CONTENT

    try:
        os.makedirs(os.path.dirname(LUA_FILTER_PATH), exist_ok=True)
        with open(LUA_FILTER_PATH, "w", encoding="utf-8") as f:
            f.write(LUA_FILTER_CONTENT)
        log_func(
            f"    Info: Lua filter created at '{os.path.basename(LUA_FILTER_PATH)}'."
        )
        return True
    except Exception as e:
        log_func(f"    ERROR: Could not write Lua filter file. Details: {e}")
        return False


def convert_step1(input_dir, log_func, dm_type):
    """
    Step 1: Executes Pandoc with the Lua filter directly on the source DOCX.
    Returns a dictionary of {filename_no_ext: adoc_content_string}.
    """
    log_func(
        "Starting Step 1: Processing source documents (DOCX -> ADOC) using Lua filter..."
    )

    if not ensure_lua_filter_exists(log_func):
        return None  # Indicate failure

    docx_files = glob.glob(os.path.join(input_dir, "*.docx"))
    if not docx_files:
        log_func("  No source documents (.docx) found. Skipping Step 1.")
        return {}  # Return empty dict if no files

    processed_adoc_content = {}

    for docx_file in docx_files:
        filename_with_ext = os.path.basename(docx_file)
        log_func(f'  - Processing: "{filename_with_ext}"')
        filename_no_ext = os.path.splitext(filename_with_ext)[0]

        if filename_no_ext.startswith("~$"):
            log_func("    ...SKIPPED (temporary file).")
            continue

        final_dmc = process_dmc_filename(filename_no_ext, log_func)
        # Placeholder header content
        if dm_type in ("sched", "schedule", "scheduled", "scheduled maintenance"):
            header_content = (
                f":dmc: {final_dmc}\n"
                f":dm-type: sched\n"
                f":tech-name: Sample System Widget Task\n"
                f":dm-title: Scheduled Maintenance Tasks\n"
                f":revdate: 2025-09-02\n"
                f":issue-number: 001\n"
                f":in-work: 00\n"
                f":lang: en\n"
                f":security-classification: 01\n"
                f":responsible-partner-company: EaseYourWork\n"
                f":enterprise-code-rpc: 8910X\n"
                f":originator-enterprise: EaseYourWork\n"
                f":enterprise-code-originator: 8910X\n"
                f":applicability: All applicable units and serial numbers.\n"
                f":brex-dmc: DMC-S1000D-H-041-1-0-0301-00-A-022-A-D\n"
                f":reason-for-update: Initial draft for demonstration purposes.\n\n"
                f"[[prelim_reqs]]\n"
                f"== Preliminary Requirements\n\n"
                f"[[sched_tasks]]\n"
                f"== Scheduled Maintenance Tasks\n"
                f". Perform a general scheduled maintenance check\n"
                f". Inspect and clean critical components\n\n"
            )
        else:
            header_content = f":dmc: {final_dmc}\n:dm-type: {dm_type}\n:tech-name: Sample System Widget Task\n:dm-title: Performing the Widget Calibration Procedure\n:revdate: 2025-09-02\n:issue-number: 001\n:in-work: 00\n:lang: en\n:security-classification: 01\n:responsible-partner-company: EaseYourWork\n:enterprise-code-rpc: 8910X\n:originator-enterprise: EaseYourWork\n:enterprise-code-originator: 8910X\n:applicability: All applicable units and serial numbers.\n:brex-dmc: DMC-S1000D-H-041-1-0-0301-00-A-022-A-D\n:reason-for-update: Initial draft for demonstration purposes.\n\n"

        try:
            # PANDOC COMMAND: Uses Lua filter
            command = [
                "pandoc",
                docx_file,
                "--lua-filter",
                LUA_FILTER_PATH,
                "-t",
                "asciidoc",
            ]

            result = subprocess.run(
                command,
                capture_output=True,
                text=True,
                check=True,
                encoding="utf-8",
                shell=True,
            )
            adoc_content = result.stdout

            # --- Post-Pandoc Cleanup (in memory) ---
            sanitized_content = re.sub(r"\+\+(.*?)\+\+", r"\1", adoc_content)
            sanitized_content = re.sub(
                r"^(\s*image):", r"\1::", sanitized_content, flags=re.MULTILINE
            )

            # Combine header and content, then apply further cleanup
            full_adoc_string = header_content + sanitized_content
            cleaned_content = cleanup_adoc_string(full_adoc_string, log_func)

            processed_adoc_content[filename_no_ext] = cleaned_content

        except FileNotFoundError:
            log_func(
                "\nERROR: Pandoc or other tool was not found. Ensure Pandoc is in your PATH."
            )
            return None
        except subprocess.CalledProcessError as e:
            log_func(
                f"\nERROR: Failed during Pandoc conversion for '{filename_with_ext}'.\n  Details:\n{e.stderr}"
            )
            return None
        except Exception as e:
            log_func(
                f"\nAn unexpected error occurred during Step 1 for '{filename_with_ext}': {e}"
            )
            return None

    return processed_adoc_content


def convert_step2(adoc_contents_dict, final_dir, log_func, dm_type):
    """
    Step 2: Converts in-memory ADOC content to S1000D XML using Asciidoctor and a custom backend.
    """
    log_func("\nStarting Step 2: Generating final S1000D files...")
    os.makedirs(final_dir, exist_ok=True)

    dm_type_key = (dm_type or "").strip().lower()
    if dm_type_key == "fault":
        ruby_script_path = RUBY_CONVERTER_SCRIPT_FAULT
    elif dm_type_key in ("sched", "schedule", "scheduled", "scheduled maintenance"):
        ruby_script_path = RUBY_CONVERTER_SCRIPT_SCHED
    else:
        ruby_script_path = RUBY_CONVERTER_SCRIPT_STANDARD

    if not adoc_contents_dict:
        log_func("  No AsciiDoc content found to process. Skipping Step 2.")
        return True

    if not os.path.exists(ruby_script_path):
        log_func(
            f"ERROR: The required converter script was not found at {ruby_script_path}"
        )
        return False

    for file_name_without_ext, adoc_content_string in adoc_contents_dict.items():
        log_func(
            f"  - Generating from in-memory content for: {file_name_without_ext}.adoc"
        )
        output_file = os.path.join(final_dir, f"{file_name_without_ext}.xml")

        command = [
            "asciidoctor",
            "--trace",
            "-r",
            ruby_script_path,
            "-b",
            "s1000d",
            "-",  # Read from stdin
            "-o",
            output_file,
        ]

        try:
            process = subprocess.run(
                command,
                input=adoc_content_string,  # Pass content to stdin
                check=True,
                capture_output=True,
                text=True,
                encoding="utf-8",
                shell=True,
            )
        except FileNotFoundError:
            log_func(
                "ERROR: Asciidoctor or Ruby not found. Ensure prerequisites are installed."
            )
            return False
        except subprocess.CalledProcessError as e:
            log_func(
                f"ERROR: Failed to generate file from '{file_name_without_ext}.adoc' during Step 2.\n  Details:\n{e.stderr}"
            )
            return False
    return True


